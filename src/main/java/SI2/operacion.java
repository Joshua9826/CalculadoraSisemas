package SI2;
public class operacion {
    public int sum(int a, int b){
        return a+b;
    }
    public int rest (int a, int b){
        return a-b;
    }
    public int multip(int a, int b){
        return a*b;
    }
    public int div(int a, int b){
        return a/b;
    }
    public int exp (int a, int b){
        return (int) Math.pow(a,b);
    }

}

